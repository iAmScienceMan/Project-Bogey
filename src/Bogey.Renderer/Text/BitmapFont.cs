    private const int FirstChar = 0x20;
    private const int LastChar = 0x7F;
    private const int GlyphCount = LastChar - FirstChar + 1; 
    private const int Columns = 16;
    private const int Rows = GlyphCount / Columns;           
    private const int AtlasWidth = Columns * GlyphWidth;     
    private const int AtlasHeight = Rows * GlyphHeight;      

    private readonly GL _gl;
    private readonly uint _texture;

    public BitmapFont(GL gl)
    {
        _gl = gl;

        byte[] pixels = BuildAtlasPixels();

        _texture = _gl.GenTexture();
        _gl.BindTexture(TextureTarget.Texture2D, _texture);
        _gl.PixelStore(PixelStoreParameter.UnpackAlignment, 1);

        ReadOnlySpan<byte> span = pixels;
        _gl.TexImage2D(TextureTarget.Texture2D, 0, InternalFormat.R8, AtlasWidth, AtlasHeight, 0,
            PixelFormat.Red, PixelType.UnsignedByte, span);

        _gl.TexParameter(TextureTarget.Texture2D, TextureParameterName.TextureMinFilter, (int)GLEnum.Nearest);
        _gl.TexParameter(TextureTarget.Texture2D, TextureParameterName.TextureMagFilter, (int)GLEnum.Nearest);
        _gl.TexParameter(TextureTarget.Texture2D, TextureParameterName.TextureWrapS, (int)GLEnum.ClampToEdge);
        _gl.TexParameter(TextureTarget.Texture2D, TextureParameterName.TextureWrapT, (int)GLEnum.ClampToEdge);
        _gl.BindTexture(TextureTarget.Texture2D, 0);
    }

    public void Bind(TextureUnit unit)
    {
        _gl.ActiveTexture(unit);
        _gl.BindTexture(TextureTarget.Texture2D, _texture);
    }

    
    public Vector4 Uv(char c)
    {
        int index = c;
        if (index < FirstChar || index > LastChar)
        {
            index = FirstChar; 
        }

        int glyph = index - FirstChar;
        int col = glyph % Columns;
        int row = glyph / Columns;

        float u0 = (float)(col * GlyphWidth) / AtlasWidth;
        float v0 = (float)(row * GlyphHeight) / AtlasHeight;
        float u1 = (float)((col + 1) * GlyphWidth) / AtlasWidth;
        float v1 = (float)((row + 1) * GlyphHeight) / AtlasHeight;
        return new Vector4(u0, v0, u1, v1);
    }

    public void Dispose() => _gl.DeleteTexture(_texture);

    private static byte[] BuildAtlasPixels()
    {
        byte[] pixels = new byte[AtlasWidth * AtlasHeight];

        for (int glyph = 0; glyph < GlyphCount; glyph++)
        {
            int col = glyph % Columns;
            int row = glyph / Columns;
            int baseX = col * GlyphWidth;
            int baseY = row * GlyphHeight;

            for (int gy = 0; gy < GlyphHeight; gy++)
            {
                byte bits = Font8x8[(glyph * GlyphHeight) + gy];
                for (int gx = 0; gx < GlyphWidth; gx++)
                {
                    if (((bits >> gx) & 1) != 0)
                    {
                        int px = baseX + gx;
                        int py = baseY + gy;
                        pixels[(py * AtlasWidth) + px] = 255;
                    }
                }
            }
        }

        return pixels;
    }

    
